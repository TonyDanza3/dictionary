public class Checker {
    public boolean checkTranslation (String userString, String dictionaryValue){
        boolean equals = false;
        int eq = userString.compareToIgnoreCase(dictionaryValue);
        if (eq == 0){
            equals = true;
        }
        if (eq == 1){
            equals = false;
        }
        return equals;
    }

    public boolean remainingWords(WordsTable scoreTable){
        int dictionarySize = scoreTable.getDictionarySize();
        boolean wordMark = true;
        boolean remainingWords = false;
        for(int i =0;i < dictionarySize;i++){
            wordMark = scoreTable.getWordMark(i);
            if (wordMark==false){
                remainingWords = true;
                break;
            }
        }
        return remainingWords;
    }
}
